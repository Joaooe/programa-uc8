
package aula04exercicio01;


public class Automovel {
    
   
        
    //definir os atribuitos do automovel
    
    private String modelo;
    private String marca;
    private String cor;
    private double velocidade;
    private int qtdecarga;

    
    public Automovel(){
    }
    
    public Automovel(String modelo, String marca, String cor, double velocidade, int qtdecarga) {
        this.modelo = modelo;
        this.marca = marca;
        this.cor = cor;
        this.velocidade = velocidade;
        this.qtdecarga = qtdecarga;
        
     
    }
    
    

        
        
}   
    

